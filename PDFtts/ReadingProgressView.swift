    // ËÆ°ÁÆóÊñáÊú¨ÊâÄÈúÄÈ´òÂ∫¶
    private func calculateTextHeight(for text: String) -> CGFloat {
        // Â¶ÇÊûúÊñáÊú¨‰∏∫Á©∫ÊàñÂæàÁü≠ÔºåËøîÂõûËæÉÂ∞èÁöÑÈªòËÆ§È´òÂ∫¶
        if text.isEmpty || text.count < 20 {
            return 120 // Á©∫ÊñáÊú¨ÊàñÁü≠ÊñáÊú¨ÁöÑÈªòËÆ§È´òÂ∫¶
        }
        
        let font = UIFont.preferredFont(forTextStyle: .body)
        let maxWidth = UIScreen.main.bounds.width - 64 // ÂáèÂéªpadding
        
        let textSize = text.boundingRect(
            with: CGSize(width: maxWidth, height: .greatestFiniteMagnitude),
            options: [.usesLineFragmentOrigin, .usesFontLeading],
            attributes: [.font: font],
            context: nil
        ).size
        
        // Âü∫Á°Äpadding + ÊñáÊú¨È´òÂ∫¶ÔºåÈôêÂà∂Âú®120-600‰πãÈó¥
        let calculatedHeight = textSize.height + 60 // 32 padding + 28 È¢ùÂ§ñÁ©∫Èó¥
        return min(max(calculatedHeight, 120), 600)
    }
    
    // Ëá™Âä®Ë∞ÉÊï¥ÊñáÊú¨Ê°ÜÈ´òÂ∫¶
    private func autoAdjustTextBoxHeight() {
        if autoAdjustHeight {
            let newHeight = calculateTextHeight(for: ttsService.currentReadingText)
            withAnimation(.easeInOut(duration: 0.3)) {
                textBoxHeight = newHeight
            }
        }
    }
    
